// operator aritmatika
let a = 10;
let b = 3;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

// operator penugasan
let c = 2;
console.log(c);

c += 5;
console.log(c);

// operator perbandingan
let d = 4;
let e = 5;

console.log(d == e);
console.log(d < e);
console.log(d > e);
console.log(d != e);

// operator logika

let f = true;
let g = false;

console.log(f && g);
console.log(f || g);
console.log(!f);

// operator bitwise
let h = 00111100;
let i = 00001101;

console.log(h & i);
console.log(h | i);
console.log(h ^ i);
console.log(~h);

// operator ternary
let j = 1;
let k = 1;

console.log(j == k ? "yes" : "no");
